import fs from 'node:fs';
import path from 'node:path';
import { createClient } from '@supabase/supabase-js';

function parseDotEnv(dotEnvPath) {
  if (!fs.existsSync(dotEnvPath)) return;
  const content = fs.readFileSync(dotEnvPath, 'utf8');
  for (const rawLine of content.split(/\r?\n/)) {
    const line = rawLine.trim();
    if (!line || line.startsWith('#')) continue;
    const eqIndex = line.indexOf('=');
    if (eqIndex <= 0) continue;
    const key = line.slice(0, eqIndex).trim();
    let value = line.slice(eqIndex + 1).trim();
    if ((value.startsWith('"') && value.endsWith('"')) || (value.startsWith("'") && value.endsWith("'"))) {
      value = value.slice(1, -1);
    }
    if (!process.env[key]) process.env[key] = value;
  }
}

function getWibNow() {
  return new Date(new Date().toLocaleString('en-US', { timeZone: 'Asia/Jakarta' }));
}

function formatDate(d) {
  const year = d.getFullYear();
  const month = String(d.getMonth() + 1).padStart(2, '0');
  const day = String(d.getDate()).padStart(2, '0');
  return `${year}-${month}-${day}`;
}

function formatTime(d) {
  const h = String(d.getHours()).padStart(2, '0');
  const m = String(d.getMinutes()).padStart(2, '0');
  return `${h}:${m}`;
}

async function run() {
  const root = process.cwd();
  parseDotEnv(path.join(root, '.env'));

  const supabaseUrl = process.env.VITE_SUPABASE_URL || process.env.SUPABASE_URL;
  const serviceRoleKey = process.env.SUPABASE_SERVICE_ROLE_KEY || process.env.SERVICE_ROLE_KEY || process.env.VITE_SUPABASE_SERVICE_ROLE_KEY;

  if (!supabaseUrl || !serviceRoleKey) {
    throw new Error('Missing Supabase env vars for test run.');
  }

  const supabase = createClient(supabaseUrl, serviceRoleKey, {
    auth: { persistSession: false, autoRefreshToken: false },
  });

  const [{ handler: enqueueHandler }, { handler: sendHandler }] = await Promise.all([
    import('../netlify/functions/enqueue-schedule-reminders.mjs'),
    import('../netlify/functions/send-telegram-notifications.mjs'),
  ]);

  const { data: channels, error: channelErr } = await supabase
    .from('notification_channels')
    .select('user_id, timezone, telegram_chat_id, is_enabled, is_verified, schedule_enabled')
    .eq('channel', 'telegram')
    .eq('is_enabled', true)
    .eq('is_verified', true)
    .eq('schedule_enabled', true)
    .not('telegram_chat_id', 'is', null)
    .limit(1);

  if (channelErr) throw channelErr;
  if (!channels || channels.length === 0) {
    throw new Error('No eligible verified Telegram channel found for schedule reminder test.');
  }

  const target = channels[0];
  const userId = target.user_id;

  const { data: scheduleRow, error: scheduleErr } = await supabase
    .from('user_schedules')
    .select('user_id, schedules_data')
    .eq('user_id', userId)
    .maybeSingle();

  if (scheduleErr) throw scheduleErr;

  const existingSchedules = Array.isArray(scheduleRow?.schedules_data) ? [...scheduleRow.schedules_data] : [];

  const reminderMinutes = Math.max(1, Number(process.env.SCHEDULE_REMINDER_MINUTES || 10));
  const wibNow = getWibNow();
  const start = new Date(wibNow.getTime() + (reminderMinutes + 1) * 60 * 1000);

  const testEventId = `e2e-schedule-test-${Date.now()}`;
  const testEvent = {
    id: testEventId,
    title: '[AUTO TEST] Schedule reminder flow check',
    description: 'Temporary event generated by test script',
    date: formatDate(start),
    startTime: formatTime(start),
    endTime: '',
    isAllDay: false,
    category: 'pribadi',
    priority: 'sedang',
    createdAt: new Date().toISOString(),
    updatedAt: new Date().toISOString(),
  };

  const nextSchedules = [...existingSchedules, testEvent];

  const upsertPayload = {
    user_id: userId,
    schedules_data: nextSchedules,
    updated_at: new Date().toISOString(),
  };

  const { error: upsertErr } = await supabase.from('user_schedules').upsert(upsertPayload, {
    onConflict: 'user_id',
    ignoreDuplicates: false,
  });
  if (upsertErr) throw upsertErr;

  let enqueueRes;
  let sendRes;
  try {
    enqueueRes = await enqueueHandler({ httpMethod: 'GET', internalCall: true, headers: {} });
    sendRes = await sendHandler({ httpMethod: 'GET', internalCall: true, headers: {} });
  } finally {
    const cleanedSchedules = existingSchedules.filter((item) => String(item?.id || '') !== testEventId);
    await supabase
      .from('user_schedules')
      .upsert({
        user_id: userId,
        schedules_data: cleanedSchedules,
        updated_at: new Date().toISOString(),
      }, { onConflict: 'user_id', ignoreDuplicates: false });
  }

  const { data: queueRows, error: queueErr } = await supabase
    .from('notification_dispatch_queue')
    .select('id, source_id, status, idempotency_key, created_at, updated_at, last_error')
    .eq('source_type', 'schedule')
    .eq('source_id', testEventId)
    .order('created_at', { ascending: false })
    .limit(5);
  if (queueErr) throw queueErr;

  const queueIds = (queueRows || []).map((row) => row.id);
  let logs = [];
  if (queueIds.length > 0) {
    const { data: logRows, error: logsErr } = await supabase
      .from('notification_dispatch_logs')
      .select('queue_id, status, attempt_number, provider_http_status, error_message, created_at')
      .in('queue_id', queueIds)
      .order('created_at', { ascending: false })
      .limit(10);
    if (logsErr) throw logsErr;
    logs = logRows || [];
  }

  const parseBody = (res) => {
    try {
      return typeof res?.body === 'string' ? JSON.parse(res.body) : (res?.body || {});
    } catch {
      return {};
    }
  };

  const result = {
    ok: true,
    targetUserId: userId,
    reminderMinutes,
    testEvent: {
      id: testEventId,
      date: testEvent.date,
      startTime: testEvent.startTime,
      wibNow: `${formatDate(wibNow)} ${formatTime(wibNow)}`,
    },
    enqueue: {
      statusCode: enqueueRes?.statusCode,
      body: parseBody(enqueueRes),
    },
    dispatch: {
      statusCode: sendRes?.statusCode,
      body: parseBody(sendRes),
    },
    queueRows,
    logs,
  };

  console.log(JSON.stringify(result, null, 2));
}

run().catch((err) => {
  console.error(JSON.stringify({ ok: false, error: err?.message || String(err) }, null, 2));
  process.exitCode = 1;
});
